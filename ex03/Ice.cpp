#include "Ice.hpp"

Ice::Ice() : AMateria("ice") {}

Ice::Ice(const Ice& src) : AMateria(src) {}

Ice& Ice::operator=(const Ice& src) {
    AMateria::operator=(src);
    return *this;
}

Ice::~Ice() {}

